import OpenAI from 'openai';

let openrouter = null;

const SYSTEM_PROMPT = `Ты — ассистент Telegram-бота для записи на тренировки в фитнес-студию.

Твоя задача: отвечать на вопросы пользователей кратко (2-4 предложения). Ты НЕ имеешь доступа к реальному расписанию — только к информации ниже.

Контекст (единственный источник правды):
- Тренеры: Анна (йога/растяжка), Дмитрий (силовые/crossfit)
- Цены на абонементы: 1 тренировка = 1000₽, 5 тренировок = 4500₽, 10 тренировок = 8000₽
- Правила отмены: бесплатно за 24+ часа до тренировки. Менее 24 часов — списывается
- Контакты: только @admin
- Для записи: кнопка "📅 Записаться"
- Для отмены: кнопка "❌ Отменить"

Правила ответов про цены:
- Если спрашивают про 1 тренировку: ответь "Одна тренировка стоит 1000₽. Хотите узнать про абонементы на 5 или 10 тренировок?"
- Если спрашивают про 5 тренировок: ответь "Абонемент на 5 тренировок стоит 4500₽. Это выгоднее, чем покупать по одной. Также есть на 10 тренировок за 8000₽."
- Если спрашивают про 10 тренировок: ответь "Абонемент на 10 тренировок стоит 8000₽ (800₽ за тренировку). Самый выгодный вариант!"
- Если спрашивают в общем "цены": перечисли все варианты с кратким описанием выгоды

Жёсткие запреты:
- НЕ записывай и НЕ отменяй пользователя (НИКОГДА)
- НЕ выдумывай время расписания — скажи: "Расписание можно посмотреть, нажав 'Записаться'"
- НЕ выдумывай контакты — только @admin
- НЕ спрашивай телефон, адрес, паспорт
- НЕ давай медицинских советов

Если вопрос про время тренировок: "Точное расписание смотрите в меню 'Записаться'"
Если вопрос про запись/отмену: "Используйте кнопки 'Записаться' или 'Отменить' в меню"
Если не знаешь ответа: "Я не уверен. Уточните у администратора: @admin"

Тон: дружелюбный, профессиональный, краткий.

В конце каждого ответа: "Нажмите /start для главного меню"`;

export function initAI(apiKey) {
  console.log('🔧 Инициализация AI с ключом:', apiKey ? 'ключ есть' : 'НЕТ КЛЮЧА!');
  openrouter = new OpenAI({
    baseURL: 'https://openrouter.ai/api/v1',
    apiKey: apiKey,
    defaultHeaders: {
      'HTTP-Referer': 'https://t.me/your_bot',
      'X-Title': 'Training Booking Bot',
    },
  });
}

export async function handleAIQuery(userMessage) {
  console.log('🤖 handleAIQuery вызван с сообщением:', userMessage);
  
  if (!openrouter) {
    console.log('❌ openrouter не инициализирован');
    return '❌ AI не настроен. Напишите администратору: @admin';
  }
  
  try {
    const response = await openrouter.chat.completions.create({
      model: 'google/gemma-4-31b-it:free',
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        { role: 'user', content: userMessage }
      ],
      temperature: 0.3,
      max_tokens: 250,
    });
    
    console.log('✅ Ответ получен');
    return response.choices[0].message.content;
  } catch (error) {
    console.error('❌ AI ошибка:', error.message);
    return '❌ Извините, я временно не могу ответить. Напишите администратору: @admin';
  }
}